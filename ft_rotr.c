#include "ft_ssl.h"

t_ullint	f_rotr5(t_ullint a, int s)
{
	a = a >> s | a << (64 - s);
	return (a);
}

t_uint		f_rotr(t_uint a, int s)
{
	a = a >> s | a << (32 - s);
	return (a);
}
