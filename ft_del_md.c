#include "ft_ssl.h"

void	ft_del_md(t_md *md)
{
	int		i;
	t_md	*tmp;

	i = 0;
	while (i < 4)
	{
		tmp = md->next;
		free(md);
		md = tmp;
		i++;
	}
}
